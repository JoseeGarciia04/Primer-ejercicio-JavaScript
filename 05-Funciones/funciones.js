// Función para sumar dos números
function suma(a, b) {
    return a + b;
  }
  
  // Función para restar dos números
  function resta(a, b) {
    return a - b;
  }
  
  // Variable que guarda el resultado de la suma
  const resultadoSuma = suma(23, 46);
  
  // Variable que guarda el resultado de la resta
  const resultadoResta = resta(64, 23);
  
  // Mostrar el resultado de la suma en la consola
  console.log("Resultado de la suma:", resultadoSuma);
  
  // Mostrar el resultado de la resta en la consola
  console.log("Resultado de la resta:", resultadoResta);
  